import UIKit

extension UIColor
{
    class func primary() -> UIColor { return UIColor(hexString: "838370") }
    class func primaryDark() -> UIColor { return UIColor(hexString: "302100") }
    class func accent() -> UIColor { return UIColor(hexString: "33AD73") }
    class func green() -> UIColor { return UIColor(hexString: "19AD63") }
    class func red() -> UIColor { return UIColor(hexString: "F52522") }
    class func white() -> UIColor { return UIColor(hexString: "FFFFFF") }
}
